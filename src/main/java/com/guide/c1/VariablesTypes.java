package com.guide.c1;

public class VariablesTypes {
  public static void main(String[] args) {
    int v;
    double y;

    v = 10;
    y = 10.0;

    System.out.println("Original value of v: " + v);
    System.out.println("Original valus of y: " + y);
    System.out.println();

    v = v/4;
    y = y/4;

    System.out.println("v after division: " + v);
    System.out.println("y after division: " + y);
  }
}
